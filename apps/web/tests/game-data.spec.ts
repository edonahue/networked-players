// Contract and correctness validation for the game artifacts, split into two
// independent concerns that must never be conflated:
//
// - The REAL Connection Guesser pool (public/data/game/*.json) is generated
//   by the Python catalog/graph-core pipeline
//   (`networked-players-catalog build-connection-rounds`), not by this
//   repo's own code. It is re-verified here from first principles two ways
//   (ADR 0043, corrective slice 4.5):
//     1. EXACT equality against universe.v1.json's own published credits[]
//        -- which is a COMPLETE per-album eligible-performer index (every
//        credit, not just the ones cited as a round's evidence), so a
//        round's answer_set/bridge_answer_sets can be checked for exact
//        agreement with an independently recomputed intersection, not just
//        containment.
//     2. A one-directional role-eligibility spot-check against
//        challenge.v2.json's own published credit rows, using an
//        independent TypeScript port of the Python performer-role allowlist
//        (not an import) -- catches a real allowlist regression that (1)
//        alone cannot, since (1) trusts the universe's own role filtering.
//        challenge.v2.json is pre-filtered for an unrelated path-discovery
//        process and routinely under-reports real credits, so this
//        direction only (real-derivable => published) is sound; the
//        opposite direction is check (1)'s job now, not this one's.
// - The SYNTHETIC test fixture (tests/fixtures/game-*.synthetic.v1.json) is
//   generated by scripts/build-rounds.mjs and exercised via --check
//   (drift + internal validation), same as before -- it is a test fixture
//   only and must never be mistaken for production data.

