test("real distractors never satisfy the connection (first principles, exact)", () => {
  const byAlbum = universePerformersByAlbum();
  const legacyByAlbum = realPerformersByAlbum();
  for (const round of roundsArtifact.rounds) {
    const answerIds = new Set(round.answer_set.map((a) => a.id));
    for (const bridgeSet of round.bridge_answer_sets ?? []) {
      for (const a of bridgeSet) answerIds.add(a.id);
    }
    for (const distractor of round.distractors) {
      expect(
        answerIds.has(distractor.id),
        `round ${round.id}: distractor ${distractor.name} is also an answer`,
      ).toBe(false);
    }
    if (round.kind === "one_hop") {
      const [a, c] = round.endpoints;
      const shared = sharedPerformers(byAlbum, a.id, c.id);
      const legacyA = legacyByAlbum.get(a.id) ?? new Set();
      const legacyC = legacyByAlbum.get(c.id) ?? new Set();
      for (const distractor of round.distractors) {
        expect(
          shared.has(distractor.id),
          `round ${round.id}: distractor ${distractor.name} is credited on both albums (universe)`,
        ).toBe(false);
        expect(
          legacyA.has(distractor.id) && legacyC.has(distractor.id),
          `round ${round.id}: distractor ${distractor.name} is credited on both albums (challenge.v2.json spot-check)`,
        ).toBe(false);
      }
    } else if (round.middle) {
      const [a, c] = round.endpoints;
      const middleId = round.middle.album.id;
      const sharedA = sharedPerformers(byAlbum, a.id, middleId);
      const sharedC = sharedPerformers(byAlbum, middleId, c.id);
      for (const distractor of round.distractors) {
        expect(
          sharedA.has(distractor.id) || sharedC.has(distractor.id),
          `round ${round.id}: distractor ${distractor.name} bridges a side`,
        ).toBe(false);
      }
    }
  }
});

