test("real pool: answer_set exactly matches the universe's own shared-performer index (first principles, exact)", () => {
  const byAlbum = universePerformersByAlbum();
  for (const round of roundsArtifact.rounds) {
    if (round.kind !== "one_hop") continue;
    const [a, c] = round.endpoints;
    const derived = sharedPerformers(byAlbum, a.id, c.id);
    const published = idSet(round.answer_set);
    expect(
      setsEqual(derived, published),
      `round ${round.id}: answer_set [${[...published]}] does not exactly match ` +
        `the universe-derived shared performers [${[...derived]}]`,
    ).toBe(true);
  }
});

test("real pool: two-hop bridge_answer_sets exactly match the universe's own shared-performer index (first principles, exact)", () => {
  const byAlbum = universePerformersByAlbum();
  for (const round of roundsArtifact.rounds) {
    if (round.kind !== "two_hop" || !round.middle || !round.bridge_answer_sets) continue;
    const [a, c] = round.endpoints;
    const middleId = round.middle.album.id;
    const derivedA = sharedPerformers(byAlbum, a.id, middleId);
    const derivedC = sharedPerformers(byAlbum, middleId, c.id);
    const [publishedA, publishedC] = round.bridge_answer_sets;
    expect(
      setsEqual(derivedA, idSet(publishedA)),
      `round ${round.id}: bridge_a [${[...idSet(publishedA)]}] does not exactly match [${[...derivedA]}]`,
    ).toBe(true);
    expect(
      setsEqual(derivedC, idSet(publishedC)),
      `round ${round.id}: bridge_c [${[...idSet(publishedC)]}] does not exactly match [${[...derivedC]}]`,
    ).toBe(true);
    // The premise of a two-hop round: no direct shared performer between the
    // two endpoints (else it should have been generated as a one-hop round).
    expect(
      sharedPerformers(byAlbum, a.id, c.id).size,
      `round ${round.id}: endpoints share a direct performer -- should be one-hop`,
    ).toBe(0);
  }
});

test("real pool: every real-derivable shared performer is a published answer (role-eligibility spot-check, subset)", () => {
  // challenge.v2.json's releases[].credits[] is EvidenceRelease-shaped: pre-
  // filtered to whichever artists happen to be endpoints of *that separate
  // artifact's own* path discovery (a different generator, a different
  // question -- see challenge.ts's EvidenceRelease comment). It routinely
  // omits a real credit the Guesser's own generator legitimately found
  // (confirmed: e.g. Futuresex/Lovesounds <-> Still Bill's real session
  // players are absent from challenge.v2.json's filtered evidence entirely).
  // So this can only check one direction safely -- but it exercises an
  // INDEPENDENT role-allowlist port the exact-equality checks above don't,
  // since those trust universe.v1.json's own (Python-filtered) credits.
  const byAlbum = realPerformersByAlbum();
  for (const round of roundsArtifact.rounds) {
    if (round.kind !== "one_hop") continue;
    const [a, c] = round.endpoints;
    const performersA = byAlbum.get(a.id) ?? new Set();
    const performersC = byAlbum.get(c.id) ?? new Set();
    const derivedShared = [...performersA].filter((id) => performersC.has(id));
    const actualAnswers = new Set(round.answer_set.map((r) => r.id));
    for (const id of derivedShared) {
      expect(
        actualAnswers.has(id),
        `round ${round.id}: artist ${id} is a real shared performer per challenge.v2.json but missing from answer_set`,
      ).toBe(true);
    }
  }
});

